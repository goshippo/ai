const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const DIGEST = path.join(ROOT, 'providers/clawhub/skills/shippo/SKILL.md');
const REFDIR = path.join(ROOT, 'skills/shippo/references');
const OUT_DIR = path.join(ROOT, 'providers/knowledge-pack');
const OUT = path.join(OUT_DIR, 'shippo-knowledge-pack.md');

const BANNER =
  '<!-- AUTO-GENERATED by scripts/build-knowledge-pack.js from the canonical skills. ' +
  'Do not edit here; edit skills/ and run `npm run sync`. -->\n\n';

const HEADER = `# Shippo Shipping Knowledge Pack

A single reference document that gives an AI assistant Shippo's shipping expertise: how to compare multi-carrier rates, buy domestic and international labels (with customs), validate addresses, track packages, run bulk batches, and analyze shipping spend, plus the field formats, rules, and gotchas that make those work reliably.

## How to use this in ChatGPT (or any assistant)

Add this file as context, then ask in plain language. Any of these work:

1. **Attach it to a chat.** Use the upload button, add this file, and ask your shipping question.
2. **Custom GPT knowledge.** Build a "Shippo Assistant" GPT and upload this as a Knowledge file so it is always available.
3. **ChatGPT Project.** Add it to a Project's files so every chat in the project can use it.

The same idea works in Claude, Gemini, and other assistants: upload or paste this document as context.

## Knowledge vs. actions (read this first)

This pack gives the assistant shipping **knowledge**. With it, the assistant can advise you, explain concepts, recommend the right workflow, and help you structure API requests. **It cannot fetch live rates or buy a real label on its own.**

To let the assistant actually *do* those things, connect the **Shippo MCP server** (the tools layer):

- In **ChatGPT**, add a custom connector (Settings, then Apps / Connectors / Plugins depending on your version) pointing at \`https://mcp.shippo.com\`, and sign in to Shippo.
- In **Claude, Cursor, and other MCP clients**, add the same server URL.

**Knowledge pack + connector together = the full experience:** the assistant knows *how* to ship and can *do* it. Where the guidance below assumes the connector, it is called out.

## Before you buy anything

- You need a **Shippo account** (free at [goshippo.com](https://goshippo.com)); for live labels you also need a payment method or connected carrier accounts.
- **Buying a label spends real money** on your Shippo account. Always confirm carrier, service, and cost before purchasing.

## A note on the "Shippo tools"

Further down, this pack refers to four Shippo tools: \`shippo_list_tools\`, \`shippo_describe_tool\`, \`shippo_read_execute_tool\`, and \`shippo_write_execute_tool\`. These exist **only if you have added the Shippo MCP connector** described above. If you are using this pack as reference only, treat them as background; the shipping guidance, field formats, and rules all still apply.

---
`;

const REF_ORDER = [
  'rate-shopping-guide', 'address-formats', 'customs-guide',
  'international-shipping', 'label-formats', 'carrier-guide',
  'csv-format', 'error-reference', 'response-envelope', 'tool-reference',
];

function stripFrontmatterAndBanner(text) {
  text = text.replace(/^---\n[\s\S]*?\n---\n+/, '');
  text = text.replace(/^<!--[\s\S]*?-->\n+/, '');
  return text.trim();
}

function main() {
  const digest = fs.readFileSync(DIGEST, 'utf8');
  const idx = digest.indexOf('## Best Practices');
  if (idx === -1) throw new Error('Could not find "## Best Practices" in the ClawHub digest. Run compose-clawhub-digest.js first.');
  let body = digest.slice(idx);

  // Reframe: the 4 MCP tools are optional (present only with the connector).
  body = body.replace(
    '### Using the Shippo MCP\n',
    '### Using the Shippo MCP (only if you have added the connector)\n\n' +
    'The four tools below exist only when the Shippo MCP connector is added (see the intro). ' +
    'If you are using this pack as reference only, treat this section as background.\n'
  );
  // Genericize client-specific sign-in commands for a cross-assistant doc.
  body = body.replace('in Claude Code, run `/mcp` and sign in', 'sign in to Shippo when your client prompts you');
  body = body.replace('in OpenClaw, `openclaw mcp login shippo`; in Claude Code, `/mcp`', "your client's sign-in prompt");
  // Redirect dangling reference-file pointers to this file's appendix.
  // Map each reference slug to its own H1 so inlined pointers stay specific
  // ("the Customs Declaration Guide section..." instead of a generic phrase).
  const refTitles = {};
  for (const name of REF_ORDER) {
    const rp = path.join(REFDIR, name + '.md');
    if (!fs.existsSync(rp)) continue;
    const m = stripFrontmatterAndBanner(fs.readFileSync(rp, 'utf8')).match(/^#\s+(.+)$/m);
    if (m) refTitles[name] = m[1].trim();
  }
  body = body.replace(/`(?:shippo\/)?references\/([a-z0-9-]+)\.md`/g, (full, slug) =>
    refTitles[slug]
      ? 'the "' + refTitles[slug] + '" section in the Reference Guides below'
      : 'the Reference Guides section below');

  const parts = [
    BANNER + HEADER,
    body.trim(),
    '\n\n---\n\n# Reference Guides\n\n' +
    'Deeper detail on specific topics. The first several are shipping domain knowledge; the last few ' +
    '(error handling, response envelope, tool catalog) are most useful when the Shippo MCP connector is connected.\n',
  ];

  for (const name of REF_ORDER) {
    const p = path.join(REFDIR, name + '.md');
    if (!fs.existsSync(p)) continue;
    let ref = stripFrontmatterAndBanner(fs.readFileSync(p, 'utf8'));
    // Demote every heading one level so the reference nests under "# Reference Guides".
    ref = ref.replace(/^(#{1,5}) /gm, (m, h) => '#' + h + ' ');
    parts.push('\n\n' + ref);
  }

  let out = parts.join('\n').replace(/\s+$/, '') + '\n';
  // Genericize any remaining Claude-Code re-auth commands for a cross-assistant doc.
  out = out.replace(/[Ii]n Claude Code, run `\/mcp` and sign in(?: again)?\./g,
    'Re-authorize the Shippo connector in your client, then sign in.');
  out = out.replace(/re-authorize via `\/mcp` if needed/g, 're-authorize the Shippo connector if needed');
  // Safety net: neutralize any leftover bare `/mcp` command mention.
  out = out.replace(/,? *(?:by )?running `\/mcp`/g, " via your client's connector settings");

  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.writeFileSync(OUT, out);
  const kb = (out.length / 1024).toFixed(0);
  console.log(`  ✓ providers/knowledge-pack/shippo-knowledge-pack.md (${out.split('\n').length} lines, ${kb} KB)`);
}

main();
